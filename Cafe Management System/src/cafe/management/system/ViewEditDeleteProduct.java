    private void BtnUpdateProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateProdukActionPerformed
        if (TxtIDProduk.getText().trim().equals("")
                || TxtNamaProduk.getText().trim().equals("")
                || TxtHargaProduk.getText().trim().equals("")
                || DtcTanggalMasuk.getDate() == null) {

            JOptionPane.showMessageDialog(this, "Isi semua data!");
            return;
        }

        try {
            // Ambil input baru
            String idProdukBaru = TxtIDProduk.getText().trim();
            String namaProdukBaru = TxtNamaProduk.getText().trim();
            Date tanggalBaru = DtcTanggalMasuk.getDate();
            int hargaBaru;

            try {
                hargaBaru = Integer.parseInt(TxtHargaProduk.getText().trim());
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(this, "Harga harus berupa angka!");
                return;
            }

            em.getTransaction().begin();

            // Cek apakah ID diganti
            if (!idProdukBaru.equals(idproduk)) {
                JOptionPane.showMessageDialog(this, "ID Produk tidak boleh diubah!");
                TxtIDProduk.setText(idproduk);
                em.getTransaction().rollback();
                return;
            }
            // Cari data lama berdasarkan ID lama
            Produk p = em.find(Produk.class, idproduk);

            if (p == null) {
                JOptionPane.showMessageDialog(this, "Produk tidak ditemukan!");
                em.getTransaction().rollback();
                return;
            }

            // Update field
            p.setNamaProduk(namaProdukBaru);
            p.setHargaProduk(hargaBaru);
            p.setTanggalMasukProduk(tanggalBaru);

            // Simpan perubahan
            em.merge(p);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data produk berhasil diperbarui!");
            showTable();
        } catch (Exception e) {
            if (em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_BtnUpdateProdukActionPerformed

