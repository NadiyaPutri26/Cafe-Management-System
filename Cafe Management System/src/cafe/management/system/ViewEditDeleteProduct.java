    private void BtnDeleteProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteProdukActionPerformed
        TxtIDProduk.setEditable(false);
        TxtNamaProduk.setEditable(false);
        TxtHargaProduk.setEditable(false);
        DtcTanggalMasuk.setEnabled(false);

        if (TxtIDProduk.getText().trim().equals("")
                || TxtNamaProduk.getText().trim().equals("")
                || TxtHargaProduk.getText().trim().equals("")
                || DtcTanggalMasuk.getDate() == null) {

            JOptionPane.showMessageDialog(this, "Pilih data dahulu!");
            return;
        } else {
            EntityManagerFactory emf = null;
            EntityManager em = null;

            try {
                emf = Persistence.createEntityManagerFactory("Cafe_Management_SystemPU");
                em = emf.createEntityManager();

                String idProduk = TxtIDProduk.getText();

                em.getTransaction().begin();
                Produk k = em.find(Produk.class, idproduk);

                if (k == null) {
                    JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                } else {
                    em.remove(k);
                    em.getTransaction().commit();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                    showTable();
                    bersih();
                }
            } catch (Exception e) {
                if (em != null && em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            } finally {
                if (em != null) {
                    em.close();
                }
                if (emf != null) {
                    emf.close();
                }
            }
        }
    }//GEN-LAST:event_BtnDeleteProdukActionPerformed

