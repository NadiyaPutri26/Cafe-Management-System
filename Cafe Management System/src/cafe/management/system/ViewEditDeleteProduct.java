    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }

        // Ambil data dari tabel
        idproduk = jTable1.getValueAt(row, 0).toString();
        namaproduk = jTable1.getValueAt(row, 1).toString();
        hargaproduk = Integer.parseInt(jTable1.getValueAt(row, 2).toString());

        try {
            tanggalmasukproduk = new SimpleDateFormat("dd-MMM-yyyy")
                    .parse(jTable1.getValueAt(row, 3).toString());
        } catch (Exception e) {
            tanggalmasukproduk = null;
        }

        // Tampilkan ke textfield
        TxtIDProduk.setText(idproduk);
        TxtNamaProduk.setText(namaproduk);
        TxtHargaProduk.setText(String.valueOf(hargaproduk));
        DtcTanggalMasuk.setDate(tanggalmasukproduk);
    }//GEN-LAST:event_jTable1MouseClicked

