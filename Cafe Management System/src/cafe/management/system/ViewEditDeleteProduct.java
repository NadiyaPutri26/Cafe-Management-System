    public void showTable() {
        DefaultTableModel tbmk;

        try {
            em.clear();
            EntityManager em = Persistence.createEntityManagerFactory("Cafe_Management_SystemPU").createEntityManager();
            Query q = em.createNamedQuery("Produk.findAll");
            List<Produk> hasil = q.getResultList();

            tbmk = new DefaultTableModel(
                    new String[]{"ID Produk", "Nama Produk", "Harga Produk", "Tanggal Masuk Produk"}, 0
            );
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(JLabel.CENTER);
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy");
            for (Produk data : hasil) {
                String tanggalFormatted = "";
                if (data.getTanggalMasukProduk() != null) {
                    tanggalFormatted = sdf.format(data.getTanggalMasukProduk());
                }
                Object[] baris = new Object[4];
                baris[0] = data.getIdProduk();
                baris[1] = data.getNamaProduk();
                baris[2] = data.getHargaProduk();
                baris[3] = tanggalFormatted;
                tbmk.addRow(baris);
            }

            jTable1.setModel(tbmk);
            jTable1.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }

